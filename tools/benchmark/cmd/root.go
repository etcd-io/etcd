package cmd

import (
	"sync"
	"time"

	"github.com/cheggaaa/pb/v3"
	"github.com/spf13/cobra"

	"go.etcd.io/etcd/client/pkg/v3/transport"
)

// This represents the base command when called without any subcommands
var RootCmd = &cobra.Command{
	Use:   "benchmark",
	Short: "A low-level benchmark tool for etcd3",
	Long: `benchmark is a low-level benchmark tool for etcd3.
It uses gRPC client directly and does not depend on
etcd client library.
		`,
}

var (
	endpoints    []string
	totalConns   uint
	totalClients uint
	precise      bool
	sample       bool

	bar *pb.ProgressBar
	wg  sync.WaitGroup

	tls transport.TLSInfo

	cpuProfPath string
	memProfPath string

	user string

	dialTimeout time.Duration

	autoSyncInterval time.Duration

	generatePerfReport bool

	benchmarkKeyPrefix string
)

func init() {
	existingPreRunE := RootCmd.PersistentPreRunE
	existingPostRunE := RootCmd.PersistentPostRunE

	cobra.OnFinalize(stopRunMetricsCapture)

	RootCmd.PersistentFlags().StringSliceVar(&endpoints, "endpoints", kiltEtcdClientEndpoints(), "gRPC endpoints")
	RootCmd.PersistentFlags().UintVar(&totalConns, "conns", 1, "Total number of gRPC connections")
	RootCmd.PersistentFlags().UintVar(&totalClients, "clients", 1, "Total number of gRPC clients")

	RootCmd.PersistentFlags().BoolVar(&precise, "precise", false, "use full floating point precision")
	RootCmd.PersistentFlags().BoolVar(&sample, "sample", false, "'true' to sample requests for every second")
	RootCmd.PersistentFlags().StringVar(&tls.CertFile, "cert", "", "identify HTTPS client using this SSL certificate file")
	RootCmd.PersistentFlags().StringVar(&tls.KeyFile, "key", "", "identify HTTPS client using this SSL key file")
	RootCmd.PersistentFlags().StringVar(&tls.TrustedCAFile, "cacert", "", "verify certificates of HTTPS-enabled servers using this CA bundle")
	RootCmd.PersistentFlags().BoolVar(&tls.InsecureSkipVerify, "insecure-skip-tls-verify", false, "skip server certificate verification")

	RootCmd.PersistentFlags().StringVar(&user, "user", "", "provide username[:password] and prompt if password is not supplied.")
	RootCmd.PersistentFlags().DurationVar(&dialTimeout, "dial-timeout", 0, "dial timeout for client connections")

	RootCmd.PersistentFlags().DurationVar(&autoSyncInterval, "auto-sync-interval", time.Duration(0), "AutoSyncInterval is the interval to update endpoints with its latest members")

	RootCmd.PersistentFlags().BoolVar(&generatePerfReport, "report-perfdash", false, "Generate benchmark report in perfdash format")
	RootCmd.PersistentFlags().StringVar(&benchmarkKeyPrefix, "key-prefix", "", "Optional prefix to prepend to keys generated by write benchmarks, useful for cleanup between runs")
	RootCmd.PersistentFlags().BoolVar(&captureRunMetrics, "capture-run-metrics", false, "Capture etcd /metrics plus local CPU, memory, disk, and network metrics during the benchmark run")
	RootCmd.PersistentFlags().StringSliceVar(&metricsURLs, "metrics-urls", nil, "HTTP/HTTPS/UNIX metrics URLs for etcd members; defaults to deriving /metrics from --endpoints")
	RootCmd.PersistentFlags().DurationVar(&metricsSampleInterval, "metrics-sample-interval", 5*time.Second, "Sampling interval used for run metrics capture")
	RootCmd.PersistentFlags().StringVar(&metricsStepName, "metrics-step-name", "", "Optional step name to annotate run metrics artifacts, useful for ramp scripts")
	RootCmd.PersistentFlags().StringVar(&metricsRunGroup, "metrics-run-group", "", "Optional group name used to correlate ramp step artifacts and elbow graphs")
	RootCmd.PersistentFlags().StringVar(&metricsOutput, "metrics-output", "", "Optional file path for the captured run metrics JSON report")
	RootCmd.PersistentFlags().StringVar(&metricsElbowGraph, "metrics-elbow-graph-output", "", "Optional HTML path to write/update an elbow graph from captured run metrics artifacts")
	RootCmd.PersistentFlags().StringVar(&metricsSummaryTable, "metrics-summary-table-output", "", "Optional Markdown path to write/update a ramp summary table from captured run metrics artifacts")
	RootCmd.PersistentFlags().StringVar(&metricsFullSummary, "metrics-full-summary-output", "", "Optional Markdown path to write/update a full summary across related run metrics artifacts")
	RootCmd.PersistentFlags().StringVar(&metricsFullSummaryHTML, "metrics-full-summary-html-output", "", "Optional HTML path to write/update a full summary across related run metrics artifacts")
	RootCmd.PersistentFlags().StringVar(&metricsAggregateRunGroup, "metrics-aggregate-run-group", "", "Optional run group prefix used for full-summary aggregation")
	RootCmd.PersistentFlags().StringSliceVar(&metricsRemoteHosts, "metrics-remote-hosts", nil, "Optional SSH targets for remote host CPU, memory, disk, and network metrics, for example opc@node1,opc@node2")
	RootCmd.PersistentFlags().DurationVar(&metricsRemoteTimeout, "metrics-remote-timeout", 4*time.Second, "Timeout for each remote host metrics SSH sample")

	RootCmd.PersistentPreRunE = func(cmd *cobra.Command, args []string) error {
		if existingPreRunE != nil {
			if err := existingPreRunE(cmd, args); err != nil {
				return err
			}
		}
		return startRunMetricsCapture(cmd)
	}

	RootCmd.PersistentPostRunE = func(cmd *cobra.Command, args []string) error {
		if existingPostRunE != nil {
			if err := existingPostRunE(cmd, args); err != nil {
				return err
			}
		}
		return nil
	}
}
